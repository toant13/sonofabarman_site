                    <label for="ac-6">Where does the money go? </label>
                    <article class="ac-600">
                   <p>All funds are transferred to our specific production bank accounts, accessible jointly by the Conservatory and the team for the following departments:</p> 
            <p>1. &nbsp; Pre-Production & Casting</p>
            <p class="indented">a. &nbsp; We have administrative tasks, location scouting, and casting to attend to before we are able to start production. This includes budgeting and scheduling Son of A Barman so that we can prepare to run a smooth, efficient set.    </p>
            
            <p>2. &nbsp; Production</p>
            <p class="indented">a. &nbsp; Money will be appropriately divided amongst these itemized categories:    </p>
            <p class="indentednumerals">i.&nbsp;   Actor & Crew Meal Fees </p>
            <p class="indentednumerals">ii.&nbsp;   Camera Rental </p>
            <p class="indentednumerals">iii.&nbsp;   Locations </p>
            <p class="indentednumerals">iV.&nbsp;   Production Design </p>
            <p class="indentednumerals">V.&nbsp;   Professional Grip/Electric Equipment </p>
            <p class="indentednumerals">Vi.&nbsp;   Transportation </p>
            
            <p>3. &nbsp; Post-Production</p>
            <p class="indented">a. &nbsp; The editing process begins <span class="orange">two days</span> into production, and then continues until the <span class="orange">end of May</span>. We plan to use the highest quality of sound design, visual effects, color, and composition to make <a class="orange" href="https://support.afi.com/thesis1314" target="_blank">Son Of A Barman</a> a complete movie experience.  
    </p>
                    </article>
                </div>
