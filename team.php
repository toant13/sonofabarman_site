                    Bennett is currently earning his MFA as a directing fellow at the American Film Institute.

He grew up in the country town of Sonoma, California. He had a love for cartoons and 

movies, and could often be found glued to the TV, building Legos, or making up games 

with his four brothers. He had an interest in creative writing, but after taking a film class 

in high school things just clicked. Bennett earned his BA in film production at Loyola 

Marymount University School of Film and Television. During his time at LMU, he 

explored lighting and grip work, experimented in the editing lab, interned at Disneynature

in Paris and settled into directing after making a short documentary in Italy for his junior 

thesis and a western short film his senior year. After his time at LMU, he worked as a 

freelance editor, while moonlighting as a skipper on the World Famous Jungle Cruise in 

Disneyland. Bennett's wide variety of experiences and work has made him a wellrounded filmmaker. As he continues to grow as a director, he still draws inspiration from

cartoons, his hometown and his family.  
