                            </div>
                           	<div class="modal-body">
                            	
                            	<p style="margin-top:0">
                                	Son of a Barman is a story about Martin coming home to the small Irish village 
                                    that he is from, and finding renewed value in the places and people he knew in the earlier 
                                    part of his life. There are many towns all over the world that are filled with smiling, 
                                    friendly faces. And from these places, come bright-eyed young people wanting to find 
                                    their way in the world. The last place these people want to end up is where they started. 
                                    They have a fear of getting stuck in their hometown and going nowhere in life. In our 
                                    story, Martin and his father, Michael, have a distant relationship. Martin's biggest fear is 
                                    returning home and doing what his father did, run the town pub. After his father's death, 
                                    he is left with the pub and must face that fear or sell the bar. Like anyone's hometown, 
                                    this one is full of wonderfully charming characters that try to help Martin change his 
                                    mind. These characters are going to bring this story to life and make you, as an audience 
                                    member, reminisce about your roots and appreciate the connections you have with loved 
                                    ones at home.
								</p>
                                <p>
                                	Martin's struggle is very personal to me. One of my biggest fears is of having to move 

                                    back home, with nothing to show for myself. I set out to Los Angeles trying to make my 
                                    
                                    own name in the film industry and I remain steadfast in continuing my pursuit. However,
                                    
                                    during my time away I have learned to appreciate where I come from and cherish the 
                                    
                                    time I am able to spend with my family and friends from home. After all, it is because of 
                                    
                                    where I come from and those who helped point my creativity towards visual storytelling 
                                    
                                    that has shaped me into the filmmaker I am today. My relationship with my parents is a 
                                    
                                    very close one. They have always supported me in my aspirations and unlike Martin, I 

									know I will always be able to look to them for inspiration.
                                </p>
                                <p>
                                	On a global level, this story is intended to show the importance of a pub to a small

                                    community. It was reported in 2012 that local pubs in the UK and Ireland are closing at 
                                    
                                    an alarming pace. The reasons stem from a struggling local and global economy, interest 
                                    
                                    changes in the younger demographics, and because of stricter enforcement of drinking and driving laws. 
                                    While it is very just cause for these laws 			to be up held, with little to no 
                                    
                                    public transportation, people in rural areas have little option to get to the town center to 
                                    
                                    socialize. These causes are keeping people at home and as a result a pivotal part in the 
                                    
                                    culture and community is fading away. Pubs are more than just bars. They are a 
                                    
                                    communal hub of culture and contact. I hope that this story inspires people to help 
                                    
                                    support their local businesses and if at all possible to encourage people to travel to visit 
                                    
                                    local pubs in smaller towns before these charming dens of spirit are gone.
								</p>
                                <p>
                                	Now every pub belongs to a town, and as I mentioned before there are towns all 

                                    over filled with colorful characters. Our town is in Ireland. We, as a team, face the 
                                    
                                    immense challenge of shooting our film in Los Angeles, and making it a living breathing 
                                    
                                    Irish village. Every person on this team does not see this as a restraint, but rather as an 
                                    
                                    invigorating test of each of our individual skills and creativity. It will push us in writing 
                                    
                                    the wonderful characters, it will make us find the perfect pub, and we will work with the 
                                    
                                    most talented cast we can to breathe life into this story. It truly is an exciting task that 
                                    
                                    lies ahead. As an AFI thesis team, we want to push ourselves to do our very best and 
                                    
                                    strive to learn as much as we can as up and coming filmmakers. We believe in this story 
                                    
                                    and I believe this team is the perfect group to tell it.
                            	</p>
                                <p>Sincerely,</p>
                                <p style="margin:.5em 0 2em 0; line-height:0; font-size:26px;">Bennett Lasseter</p>        
